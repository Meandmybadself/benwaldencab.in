/*
 * Benwalden service worker
 * -------------------------
 * Goal: aggressive caching so the site works fully offline, while online
 * visitors always get the freshest content and live data.
 *
 * Strategy by request type:
 *   - HTML navigations         -> network-first (fresh when online, cached offline)
 *   - Live JSON feeds (APIs)   -> network-first (fresh when online, last-known offline)
 *   - Static assets & fonts    -> stale-while-revalidate (instant + refreshed in bg)
 *
 * Bump CACHE_VERSION whenever the precached shell changes so old caches are
 * purged on activation.
 */

const CACHE_VERSION = 'v3';
const PRECACHE = `benwalden-precache-${CACHE_VERSION}`;
const RUNTIME = `benwalden-runtime-${CACHE_VERSION}`;

