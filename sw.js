// Hosts serving live data — always try the network first so online visitors
// see up-to-date weather / bird / business info, falling back to cache offline.
const API_HOSTS = [
  'weather.benwaldencab.in',
  'birdweather.benwaldencab.in',
  'admin.benwaldencab.in',
];

// ---- Install: precache the app shell -------------------------------------
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(PRECACHE).then((cache) =>
      // addAll is atomic (any 404 aborts the whole install), so add items
      // individually and tolerate the occasional missing asset.
      Promise.allSettled(
        PRECACHE_URLS.map((url) =>
          cache.add(new Request(url, { cache: 'reload' }))
        )
      )
    )
  );
  // Activate this new worker as soon as it finishes installing.
  self.skipWaiting();
});

// ---- Activate: drop stale caches -----------------------------------------
self.addEventListener('activate', (event) => {
  const keep = new Set([PRECACHE, RUNTIME]);
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(keys.filter((k) => !keep.has(k)).map((k) => caches.delete(k)))
      )
      .then(() => self.clients.claim())
  );
});

// Allow the page to tell a waiting worker to take over immediately.
self.addEventListener('message', (event) => {
  if (event.data === 'SKIP_WAITING') self.skipWaiting();
});

// ---- Fetch routing --------------------------------------------------------
self.addEventListener('fetch', (event) => {
  const { request } = event;

  // Only handle GETs; leave POSTs etc. to the network untouched.
  if (request.method !== 'GET') return;

  let url;
  try {
    url = new URL(request.url);
  } catch (e) {
    return;
  }

  // Only handle http(s) — skip chrome-extension:, data:, etc.
  if (url.protocol !== 'http:' && url.protocol !== 'https:') return;

  // HTML navigations -> network-first.
  if (
    request.mode === 'navigate' ||
    (request.headers.get('accept') || '').includes('text/html')
  ) {
    event.respondWith(networkFirst(request, PRECACHE, '/index.html'));
    return;
  }

  // Live data feeds -> network-first.
  if (API_HOSTS.includes(url.hostname)) {
    event.respondWith(networkFirst(request, RUNTIME));
    return;
  }

  // Everything else (assets, fonts, css) -> stale-while-revalidate.
  event.respondWith(staleWhileRevalidate(request, RUNTIME));
});

// ---- Strategies -----------------------------------------------------------

// Network-first: return the live response when reachable and cache it;
// otherwise serve the cached copy (or an optional fallback URL).
async function networkFirst(request, cacheName, fallbackUrl) {
  const cache = await caches.open(cacheName);
  try {
    const response = await fetch(request);
    if (response && (response.ok || response.type === 'opaque')) {
      cache.put(request, response.clone());
    }
    return response;
  } catch (e) {
    const cached = await cache.match(request);
    if (cached) return cached;
    if (fallbackUrl) {
      const fallback = await caches.match(fallbackUrl);
      if (fallback) return fallback;
    }
    return Response.error();
  }
}

// Stale-while-revalidate: serve cache immediately (fast + offline) and
// refresh the cached copy in the background for next time.
async function staleWhileRevalidate(request, cacheName) {
  const cache = await caches.open(cacheName);
  const cached = await cache.match(request);

  const network = fetch(request)
    .then((response) => {
      if (response && (response.ok || response.type === 'opaque')) {
        cache.put(request, response.clone());
      }
      return response;
    })
    .catch(() => null);

  return cached || (await network) || Response.error();
}
